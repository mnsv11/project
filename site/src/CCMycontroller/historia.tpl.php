<h3>Historia</h3>
<p>1909 grundade Michio Suzuki Suzuki Loom Company i Hamamatsu i Japan. 
Företaget ägnade sig från början åt att tillverka vävstolar. 1952 började man tillverka hjälpmotorer för trampcyklar.
Sedan 2009 samarbetar man med tyska Volkswagen som äger 19,9% av företaget. Suzuki är idag Japans fjärde största biltillverkare.</p>
 
<h5>Tvåtaktare</h5>
 
<p>Under 60- och 70-talen var Suzuki känt för sina tvåtaktare. Först kom T-serien med tvåcylindriga motorer, 
tex T20(250cc) och T500(500cc). T-serien följdes av GT-serien med 2- och 3-cylindriga motorer. 
Till exempel var GT250 efterföljare till T20, och T500 följdes av GT500. 
GT-seriens trecylindriga modeller utgjordes av GT380, GT550 samt GT750. GT750 var Suzukis första vattenkylda motorcykel.
<br><br>
Trots att motorcyklar med fyrtaktsmotor hade blivit dominerande under senare delen av 70-talet, 
så fortsatte man hos Suzuki med tvåtaktare under 80-talet med RG-modellerna. 
RG 500 som var den största och hade en mycket avancerad tvåtaktsmotor, 
en vätskekyld s.k kvadratfyra med två vevaxlar och slidmatning. Suzukis tvåtaktare fick ett 
gott anseende tack vare att de ansågs ha en bättre tillförlitlighet än konkurrenterna. 
De flesta av Suzukis tvåtaktsmotorcyklar är idag eftertraktade av samlare och entusiaster</p>
 
<h5>Wankel</h5>
<p>I början av 70-talet var wankelmotorn på modet. Många bil- och motorcykelfabrikanter 
tog fram modeller med wankelmotorer. Den motorcykelfabrik som satsade mest på wankelmotorn var Suzuki. 
Suzuki RE5 (500cc tvåskivig wankel) fick ett gott mottagande i motorpressen. 
Motorn hade en silkeslen gång och goda prestanda. Modellen blev dock en försäljningsmässig flopp, 
trots ihärdig marknadsföring tvingades man lägga ner modellen. Rykten säger att man dumpade det sista 
lagret av fabriksnya motorcyklar och reservdelar i japanska sjön.</p>
 
<h5>Fyrtaktare</h5>
<p>Efter fiaskot med wankelmotorn insåg Suzuki att man måste ta fram en fyrcylindrig fyrtaktare. 
1976 var GS750 klar, fabrikens första fyrtaktsmodell. Den hade dubbla överliggande kamaxlar och 
två ventiler per cylinder. Senare följdes den av GS500 och GS1000. Utmärkande för dessa modeller 
var god tillförlitlighet och goda vägegenskaper. GS850G var Suzukis första motorcykelmodell med kardan. 
Den följdes så småningom av GS1000G.
<br><br>
1979 kom GSX1100E, en moderniserad och förstorad variant av GS1000. Med sin 16-ventilsmotor 
var den modern för sin tid. Tack vare en speciell utformning av förbränningsrummen 
(TSCC - Twin Swirl Combustion Chamber) fick modellen god bränsleekonomi och hög prestanda. 
Modellen blev en stor succé, under några år på 80-talet var modellen den mest sålda superbiken i Sverige. 
GSX1100S (katana) var en aggressivt designad variant i serien. Senare utökades GSX-serien med 
mindre modeller till exempel GSX750 och GSX550. Motorerna hos de tidiga GSX1100-modellerna hade 
synnerligen god hållbarhet. Detta berodde delvis på motorernas relativt enkla konstruktion. 
Motorernas rullagrade vevaxlar ställde till exempel inte lika höga krav på smörjsystemet som 
konkurrenternas glidlagrade. GSX1100-motorerna har använts flitigt i bland annat i dragracing.
